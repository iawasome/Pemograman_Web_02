        $(document).ready(function() {
            $('#kabkota').change(function() {
                var kabkotaId = $(this).val();
                $.ajax({
                    url: '/faskes/' + kabkotaId,
                    type: 'GET',
                    success: function(data) {
                        var faskesList = $('#faskes-list');
                        faskesList.empty();
                        $.each(data, function(index, faskes) {
                            faskesList.append('<li>' + faskes.nama + ' - ' + faskes.alamat + '</li>');
                        });
                    }
                });
            });
        });
